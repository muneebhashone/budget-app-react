export default function getCurrentMonthWithYear() {
  const month = [
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December",
  ];

  const d = new Date();
  return `${month[d.getMonth()]} ${d.getFullYear()}`;
}
